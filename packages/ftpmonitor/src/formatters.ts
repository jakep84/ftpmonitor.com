type StepKey = "dns" | "tcp" | "auth" | "list";
type Protocol = "ftp" | "ftps" | "sftp";

export type Step = {
  key: StepKey;
  ok: boolean;
  ms?: number;
  message: string;
  details?: Record<string, any>;
};

export type Result = {
  ok: boolean;
  protocol: Protocol;
  host: string;
  port: number;
  testedPath?: string;
  totalMs: number;
  steps: Step[];
  tips: string[];
};

function padRight(s: string, n: number) {
  return (s + " ".repeat(n)).slice(0, n);
}
function ms(v?: number) {
  return typeof v === "number" ? `${v}ms` : "";
}
function okWord(ok: boolean) {
  return ok ? "OK" : "FAIL";
}
function emoji(ok: boolean) {
  return ok ? "✅" : "❌";
}

export function toPretty(r: Result, helpUrl?: string) {
  const lines: string[] = [];
  lines.push("FTPMonitor");
  lines.push(`Host: ${r.host}`);
  lines.push(
    `Protocol: ${r.protocol.toUpperCase()}  Port: ${r.port}${r.testedPath ? `  Path: ${r.testedPath}` : ""}`,
  );
  lines.push("");

  for (const s of r.steps) {
    lines.push(
      `${padRight(s.key.toUpperCase(), 5)} ${padRight(emoji(s.ok), 2)} ${padRight(ms(s.ms), 8)} ${s.message}`,
    );
  }

  if (r.tips?.length) {
    lines.push("");
    lines.push("Tips:");
    for (const t of r.tips) lines.push(`- ${t}`);
  }

  lines.push("");
  lines.push(`Total: ${r.totalMs}ms`);
  if (helpUrl) lines.push(`Troubleshooting: ${helpUrl}`);
  lines.push(
    "Diagnose your FTP/FTPS/SFTP endpoints instantly: https://ftpmonitor.com",
  );
  return lines.join("\n");
}

