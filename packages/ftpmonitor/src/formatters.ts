export function toJira(r: Result, helpUrl?: string) {
  const rows = r.steps
    .map(
      (s) =>
        `${padRight(s.key.toUpperCase(), 5)} ${padRight(okWord(s.ok), 4)} ${padRight(ms(s.ms), 8)} ${s.message}`,
    )
    .join("\n");

  const header =
    `h3. FTPMonitor Check\n` +
    `*Host:* ${r.host}\n` +
    `*Protocol:* ${r.protocol.toUpperCase()}  *Port:* ${r.port}${r.testedPath ? `  *Path:* ${r.testedPath}` : ""}\n\n` +
    `{code}\n${rows}\n{code}\n`;

  const tail = helpUrl ? `\n*Troubleshooting:* ${helpUrl}\n` : "\n";
  return header + tail;
}

export function toMarkdown(r: Result, helpUrl?: string) {
  const rows = r.steps
    .map(
      (s) =>
        `${padRight(s.key.toUpperCase(), 5)} ${padRight(okWord(s.ok), 4)} ${padRight(ms(s.ms), 8)} ${s.message}`,
    )
    .join("\n");

  return (
    `## FTPMonitor Check\n` +
    `- Host: ${r.host}\n` +
    `- Protocol: ${r.protocol.toUpperCase()}\n` +
    `- Port: ${r.port}\n` +
    (r.testedPath ? `- Path: ${r.testedPath}\n` : "") +
    `\n\`\`\`text\n${rows}\n\`\`\`\n\n` +
    (r.tips?.length
      ? `**Tips**\n${r.tips.map((t) => `- ${t}`).join("\n")}\n\n`
      : "") +
    (helpUrl ? `Troubleshooting: ${helpUrl}\n` : "") +
    `Generated by ftpmonitor.com\n`
  );
}
